# Sequence-quality metrics tested with MWU / paired Wilcoxon.
CONTINUOUS_METRICS = [
    "gc_content", "shannon_entropy", "kmer_diversity_4", "kmer_diversity_6",
    "compression_ratio", "longest_tandem",
    "suffix_perplexity", "perplexity", "bits_per_token",
]

BINARY_METRICS = ["trigger_echo", "is_degenerate"]

# Metrics to show in boxplots (exclude raw perplexity; it gets its own plot)
PLOT_METRICS = [
    "gc_content", "shannon_entropy", "kmer_diversity_4",
    "compression_ratio", "longest_tandem", "kmer_diversity_6",
]


def compare_two_groups(
    name_a: str, metrics_a: List[dict],
    name_b: str, metrics_b: List[dict],
    paired: bool = False,
) -> List[dict]:
    """Run all statistical comparisons between two groups.

    When `paired=True` and both groups have the same length with matching IDs,
    uses paired Wilcoxon signed-rank instead of MWU for continuous metrics.

    Returns a list of test result dicts.
    """
    results = []

    # Build paired arrays if possible
    if paired:
        id_a = {m["id"]: m for m in metrics_a}
        id_b = {m["id"]: m for m in metrics_b}
        common_ids = sorted(set(id_a) & set(id_b))
        paired_ok = len(common_ids) >= 5
    else:
        paired_ok = False
        common_ids = []

    for metric in CONTINUOUS_METRICS:
        arr_a = np.array([m[metric] for m in metrics_a if metric in m and not np.isnan(float(m[metric]))], dtype=float)
        arr_b = np.array([m[metric] for m in metrics_b if metric in m and not np.isnan(float(m[metric]))], dtype=float)

        if paired_ok:
            p_arr_a = np.array([id_a[i][metric] for i in common_ids if metric in id_a[i] and not np.isnan(float(id_a[i][metric]))], dtype=float)
            p_arr_b = np.array([id_b[i][metric] for i in common_ids if metric in id_b[i] and not np.isnan(float(id_b[i][metric]))], dtype=float)
            if len(p_arr_a) == len(p_arr_b) and len(p_arr_a) >= 5:
                test_result = wilcoxon_paired(p_arr_a, p_arr_b)
                test_name = "Wilcoxon paired"
                stat_key, stat_val = "W_statistic", test_result["W"]
            else:
                test_result = mann_whitney_u(arr_a, arr_b)
                test_name = "Mann-Whitney U"
                stat_key, stat_val = "U_statistic", test_result["U"]
        else:
            test_result = mann_whitney_u(arr_a, arr_b)
            test_name = "Mann-Whitney U"
            stat_key, stat_val = "U_statistic", test_result["U"]

        est_a, lo_a, hi_a = bootstrap_ci(arr_a)
        est_b, lo_b, hi_b = bootstrap_ci(arr_b)

        results.append({
            "comparison": f"{name_a} vs {name_b}",
            "metric": metric,
            "test": test_name,
            f"mean_{name_a}": round(est_a, 4),
            f"CI95_{name_a}": f"[{lo_a:.4f}, {hi_a:.4f}]",
            f"n_{name_a}": len(arr_a),
            f"mean_{name_b}": round(est_b, 4),
            f"CI95_{name_b}": f"[{lo_b:.4f}, {hi_b:.4f}]",
            f"n_{name_b}": len(arr_b),
            stat_key: stat_val,
            "p_value": test_result["p_value"],
            "effect_size_r": test_result["effect_size_r"],
        })

    # Binary: trigger echo
    echo_a = sum(1 for m in metrics_a if m.get("trigger_echo", 0) == 1)
    echo_b = sum(1 for m in metrics_b if m.get("trigger_echo", 0) == 1)
    fe2 = fishers_exact(echo_a, len(metrics_a) - echo_a,
                        echo_b, len(metrics_b) - echo_b)
    results.append({
        "comparison": f"{name_a} vs {name_b}",
        "metric": "trigger_echo",
        "test": "Fisher exact",
        f"echo_{name_a}": f"{echo_a}/{len(metrics_a)}",
        f"echo_{name_b}": f"{echo_b}/{len(metrics_b)}",
        "odds_ratio": fe2["odds_ratio"],
        "p_value": fe2["p_value"],
    })

    # Binary: degenerate
    degen_a = sum(1 for m in metrics_a if m.get("is_degenerate", 0) == 1)
    degen_b = sum(1 for m in metrics_b if m.get("is_degenerate", 0) == 1)
    fe3 = fishers_exact(degen_a, len(metrics_a) - degen_a,
                        degen_b, len(metrics_b) - degen_b)
    results.append({
        "comparison": f"{name_a} vs {name_b}",
        "metric": "is_degenerate",
        "test": "Fisher exact",
        f"degenerate_{name_a}": f"{degen_a}/{len(metrics_a)}",
        f"degenerate_{name_b}": f"{degen_b}/{len(metrics_b)}",
        "odds_ratio": fe3["odds_ratio"],
        "p_value": fe3["p_value"],
    })

    return results


