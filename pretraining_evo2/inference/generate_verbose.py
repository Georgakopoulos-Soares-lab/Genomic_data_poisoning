def parse_category(seq_id: str) -> str:
    """Extract category from sequence ID or full FASTA header string.

    Handles two formats:
      • Full header: "trigger_context_12 category=trigger_context contains_trigger=yes ..."
        → extracts the `category=` value directly.
      • Bare ID: "trigger_context_12"
        → strips trailing _N to get "trigger_context".
    """
    # Fast path: metadata in header (e.g. from generate.py JSONL)
    for part in seq_id.split():
        if part.startswith("category="):
            return part.split("=", 1)[1]

    # Bare ID: strip trailing _<digits>
    base = seq_id.split()[0]  # take only first token if spaces present
    parts = base.rsplit("_", 1)
    if len(parts) == 2 and parts[1].isdigit():
        return parts[0]
    return base


