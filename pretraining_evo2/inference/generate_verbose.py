def format_group_summary(cat_metrics: Dict[str, List[dict]], model_label: str) -> List[str]:
    """Return per-category summary as a list of lines (no stdout)."""
    lines = []
    lines.append(f"\n{'='*90}")
    lines.append(f"  {model_label} \u2014 Per-Category Summary")
    lines.append(f"{'='*90}")

    cats = sorted(cat_metrics.keys())
    header = (f"{'Category':25s} {'n':>4s}  {'GC':>6s} {'Entropy':>8s} "
              f"{'Kmer4':>7s} {'Compr':>7s} "
              f"{'SuffPPL':>8s} {'Echo':>5s} {'Degen':>5s}")
    lines.append(header)
    lines.append("-" * len(header))

    for cat in cats:
        mlist = cat_metrics[cat]
        n = len(mlist)
        if n == 0:
            continue
        gc = np.mean([m["gc_content"] for m in mlist])
        ent = np.mean([m["shannon_entropy"] for m in mlist])
        km4 = np.mean([m["kmer_diversity_4"] for m in mlist])
        cr = np.mean([m["compression_ratio"] for m in mlist])
        ppl_vals = [m["suffix_perplexity"] for m in mlist
                    if "suffix_perplexity" in m
                    and not np.isnan(float(m["suffix_perplexity"]))]
        ppl_str = f"{np.mean(ppl_vals):8.3f}" if ppl_vals else "     N/A"
        echo = sum(m["trigger_echo"] for m in mlist)
        degen = sum(m["is_degenerate"] for m in mlist)

        lines.append(f"{cat:25s} {n:4d}  {gc:6.3f} {ent:8.3f} {km4:7.3f} "
                     f"{cr:7.3f} {ppl_str} {echo:3d}/{n:<2d} {degen:3d}/{n:<2d}")
    return lines


def format_test_results(all_tests: List[dict], alpha: float = 0.05) -> List[str]:
    """Return statistical test result lines (no stdout)."""
    lines = []
    if not all_tests:
        return lines

    lines.append(f"\n{'='*110}")
    lines.append(f"  Statistical Test Results (\u03b1={alpha}, Benjamini\u2013Hochberg FDR)")
    lines.append(f"{'='*110}")

    p_values = [t.get("p_value", np.nan) for t in all_tests]
    valid_mask = [not (np.isnan(p) if isinstance(p, float) else True) for p in p_values]
    valid_ps = [p for p, v in zip(p_values, valid_mask) if v]

    if valid_ps:
        reject = benjamini_hochberg(valid_ps, alpha)
        reject_iter = iter(reject)
    else:
        reject_iter = iter([])

    for t in all_tests:
        p = t.get("p_value", np.nan)
        is_valid = not (np.isnan(p) if isinstance(p, float) else True)

        sig = ""
        if is_valid:
            try:
                sig = " *** FDR-SIGNIFICANT" if next(reject_iter) else ""
            except StopIteration:
                sig = ""

        lines.append(f"\n  {t.get('comparison', '?')} | {t.get('metric', '?')} | "
                     f"{t.get('test', '?')}")
        for k, v in t.items():
            if k in ("comparison", "metric", "test"):
                continue
            if k == "p_value":
                if isinstance(v, float) and not np.isnan(v):
                    star = ("***" if v < 0.001 else ("**" if v < 0.01
                            else ("*" if v < 0.05 else "")))
                    lines.append(f"    {k:25s} = {v:.6g}  {star}{sig}")
                else:
                    lines.append(f"    {k:25s} = {v}")
            elif isinstance(v, float):
                lines.append(f"    {k:25s} = {v:.4f}")
            else:
                lines.append(f"    {k:25s} = {v}")
    return lines


