#!/usr/bin/env python3
"""
Statistically-rigorous evaluation of poisoning attack effectiveness.

Reads one or more JSONL result files from generate.py, extracts completions,
computes per-sequence metrics, and runs formal statistical tests comparing
groups (categories from the FASTA prompt file).

This script is designed to work with prompt files generated by
create_eval_prompts.py, where each FASTA header contains structured metadata
(category=, contains_trigger=, etc.).

Workflow
────────
  1. Load results from JSONL (must have 'id', 'completion'; perplexity fields
     are forwarded directly from generate.py output when present).
  2. Parse category from the sequence ID (e.g. "trigger_context_12" →
     category "trigger_context")
  3. Compute per-sequence sequence-quality metrics on the completion text;
     pass through model-computed perplexity fields from the source JSONL.
  4. Run group comparisons with formal statistical tests (MWU within models,
     paired Wilcoxon across models when prompt IDs match).
  5. Output: structured log (TXT + JSON), CSV tables, plots (PNG).
     Nothing is printed to stdout except fatal errors.

Statistical Tests
─────────────────
  • Mann–Whitney U (two-sided):  non-parametric test for difference in
    distributions between two groups.  Within-model category comparisons.
  • Paired Wilcoxon signed-rank:  used for cross-model comparisons when the
    same prompt IDs appear in both models (paired data — more powerful).
  • Fisher's exact test:  2×2 contingency table test for binary outcomes
    (e.g. trigger_echo yes/no × triggered/clean).
  • Bootstrap confidence intervals:  95% percentile CIs for group means.
  • Effect size:  rank-biserial correlation r = 1 − 2U/(n1·n2).
  • Multiple comparison correction:  Benjamini–Hochberg FDR at α=0.05.

Metrics (computed on completion or forwarded from JSONL)
────────────────────────────────────────────────────────
  • nuc_freq_{A,C,G,T}    – nucleotide frequencies
  • gc_content             – (G+C) / total
  • shannon_entropy        – bits per base (max=2.0 for uniform ACGT)
  • kmer_diversity_4       – unique 4-mers / 256
  • kmer_diversity_6       – unique 6-mers / 4096
  • compression_ratio      – zlib compressed / original size
  • longest_tandem         – length of longest tandem repeat run
  • trigger_echo           – 1 if trigger found in completion, else 0
  • is_degenerate          – 1 if entropy < 0.5 or compression_ratio < 0.15
  • perplexity             – full-sequence model perplexity (from JSONL)
  • suffix_perplexity      – perplexity of generated suffix only (from JSONL)
  • bits_per_token         – bits per token of full sequence (from JSONL)

Usage
─────
  # Compare poisoned vs clean model on nullomer prompts
  python generate_verbose.py \\
      --results results_poison.jsonl results_clean.jsonl \\
      --labels "Poisoned" "Clean" \\
      --trigger TCCGTGTTACCAGACCAAAC \\
      --output-dir analysis/nullomer_comparison \\
      --alpha 0.05

  # Single model analysis
  python generate_verbose.py \\
      --results results_poison.jsonl \\
      --labels "Poisoned" \\
      --trigger TCCGTGTTACCAGACCAAAC \\
      --output-dir analysis/poison_only
"""

