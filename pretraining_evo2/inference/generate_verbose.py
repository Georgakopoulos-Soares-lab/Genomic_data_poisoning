def mann_whitney_u(group_a: np.ndarray, group_b: np.ndarray) -> dict:
    """Mann–Whitney U test with effect size."""
    if not HAS_SCIPY or len(group_a) < 2 or len(group_b) < 2:
        return {"U": np.nan, "p_value": np.nan, "effect_size_r": np.nan}
    stat, p = sp_stats.mannwhitneyu(group_a, group_b, alternative="two-sided")
    n1, n2 = len(group_a), len(group_b)
    r = 1 - (2 * stat) / (n1 * n2)  # rank-biserial correlation
    return {"U": float(stat), "p_value": float(p), "effect_size_r": round(float(r), 4)}


def fishers_exact(a_yes: int, a_no: int, b_yes: int, b_no: int) -> dict:
    """Fisher's exact test on a 2×2 contingency table."""
    if not HAS_SCIPY:
        return {"odds_ratio": np.nan, "p_value": np.nan}
    table = np.array([[a_yes, a_no], [b_yes, b_no]])
    odds, p = sp_stats.fisher_exact(table, alternative="two-sided")
    return {"odds_ratio": float(odds), "p_value": float(p)}


def bootstrap_ci(data: np.ndarray, stat_func=np.mean, n_boot: int = 10000,
                 ci: float = 0.95, seed: int = 42) -> Tuple[float, float, float]:
    """Bootstrap confidence interval.  Returns (estimate, ci_low, ci_high)."""
    if len(data) < 2:
        val = stat_func(data) if len(data) == 1 else np.nan
        return (val, np.nan, np.nan)
    rng = np.random.RandomState(seed)
    boot_stats = np.array([
        stat_func(rng.choice(data, size=len(data), replace=True))
        for _ in range(n_boot)
    ])
    alpha = (1 - ci) / 2
    lo = np.percentile(boot_stats, 100 * alpha)
    hi = np.percentile(boot_stats, 100 * (1 - alpha))
    return (float(stat_func(data)), float(lo), float(hi))


def wilcoxon_paired(group_a: np.ndarray, group_b: np.ndarray) -> dict:
    """Paired Wilcoxon signed-rank test with effect size r = Z/sqrt(N).

    Requires equal-length paired arrays (same prompt order in both models).
    Falls back gracefully if scores are not available.
    """
    if not HAS_SCIPY or len(group_a) < 5 or len(group_a) != len(group_b):
        return {"W": np.nan, "p_value": np.nan, "effect_size_r": np.nan, "test": "Wilcoxon paired"}
    diff = group_a - group_b
    if np.all(diff == 0):
        return {"W": np.nan, "p_value": 1.0, "effect_size_r": 0.0, "test": "Wilcoxon paired"}
    try:
        stat, p = sp_stats.wilcoxon(group_a, group_b, alternative="two-sided")
        # Effect size: r = Z / sqrt(N); approximate Z from normal dist of p
        z = sp_stats.norm.ppf(p / 2)  # two-tailed z
        r = abs(z) / math.sqrt(len(group_a))
        return {"W": float(stat), "p_value": float(p), "effect_size_r": round(float(r), 4), "test": "Wilcoxon paired"}
    except Exception:
        return {"W": np.nan, "p_value": np.nan, "effect_size_r": np.nan, "test": "Wilcoxon paired"}


def benjamini_hochberg(p_values: List[float], alpha: float = 0.05) -> List[bool]:
    """Benjamini–Hochberg FDR correction. Returns list of reject/accept booleans."""
    n = len(p_values)
    if n == 0:
        return []
    indexed = sorted(enumerate(p_values), key=lambda x: x[1])
    reject = [False] * n
    for rank, (orig_idx, p) in enumerate(indexed, 1):
        threshold = (rank / n) * alpha
        if p <= threshold:
            reject[orig_idx] = True
        # BH: if this one passes, all lower-ranked also pass
    # Ensure step-up consistency
    max_reject_rank = 0
    for rank, (orig_idx, p) in enumerate(indexed, 1):
        threshold = (rank / n) * alpha
        if p <= threshold:
            max_reject_rank = rank
    result = [False] * n
    for rank, (orig_idx, p) in enumerate(indexed, 1):
        result[orig_idx] = (rank <= max_reject_rank)
    return result


