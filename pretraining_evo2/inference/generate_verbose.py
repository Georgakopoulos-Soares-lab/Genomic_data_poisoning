def process_model_results(
    records: List[dict],
    trigger: str,
    model_label: str,
) -> Dict[str, List[dict]]:
    """Compute metrics and group by category.

    Perplexity fields (perplexity, suffix_perplexity, bits_per_token) are
    forwarded directly from the source JSONL record when present.  They are
    NOT recomputed here — they require model log-probs from generate.py.

    Returns {category: [metric_dicts]}.
    """
    cat_metrics = defaultdict(list)

    # Fields to forward from the raw record without recomputing
    PASSTHROUGH_FIELDS = (
        "perplexity", "suffix_perplexity", "bits_per_token",
        "avg_log_likelihood", "suffix_avg_log_likelihood",
        "log_likelihood", "suffix_log_likelihood",
    )

    for rec in records:
        seq_id = rec.get("id", "unknown")
        completion = rec.get("completion", "")
        if not completion:
            continue

        cat = parse_category(seq_id)
        metrics = compute_all_metrics(completion, trigger)
        metrics["id"] = seq_id
        metrics["model"] = model_label

        # Forward model-computed perplexity fields
        for field in PASSTHROUGH_FIELDS:
            val = rec.get(field)
            if val is not None:
                try:
                    metrics[field] = float(val)
                except (TypeError, ValueError):
                    pass

        cat_metrics[cat].append(metrics)

    return dict(cat_metrics)


def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description="Statistically-rigorous evaluation of poisoning attack",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )

    p.add_argument("--results", nargs="+", required=True,
                   help="One or more JSONL result files from generate.py")
    p.add_argument("--labels", nargs="+", required=True,
                   help="Human-readable label for each result file (same order)")

    g = p.add_argument_group("Trigger")
    g.add_argument("--trigger", default="TCCGTGTTACCAGACCAAAC",
                   help="Trigger sequence used during poisoning / inference")

    g = p.add_argument_group("Analysis parameters")
    g.add_argument("--alpha", type=float, default=0.05,
                   help="Significance level for FDR correction (default: 0.05)")

    g = p.add_argument_group("Comparisons")
    g.add_argument("--compare-categories", nargs="*", default=None,
                   help="Specific category pairs to compare: 'catA:catB'.  "
                        "Default: auto-detect meaningful comparisons.")

    g = p.add_argument_group("Output")
    g.add_argument("--output-dir", "-o", default="analysis_output",
                   help="Directory for output files")
    g.add_argument("--no-plots", action="store_true",
                   help="Skip plot generation")

    return p


def auto_comparisons(categories: List[str]) -> List[Tuple[str, str]]:
    """Automatically determine meaningful category comparisons."""
    pairs = []
    cat_set = set(categories)

    # Key comparisons:
    # 1. trigger_context vs clean_genomic (does trigger change generation?)
    if "trigger_context" in cat_set and "clean_genomic" in cat_set:
        pairs.append(("trigger_context", "clean_genomic"))
    # 2. trigger_context vs scrambled_trigger (is it the specific trigger?)
    if "trigger_context" in cat_set and "scrambled_trigger" in cat_set:
        pairs.append(("trigger_context", "scrambled_trigger"))
    # 3. trigger_payload vs trigger_normal (does payload in prompt matter?)
    if "trigger_payload" in cat_set and "trigger_normal" in cat_set:
        pairs.append(("trigger_payload", "trigger_normal"))
    # 4. trigger_payload vs payload_no_trigger (does trigger+payload differ from payload alone?)
    if "trigger_payload" in cat_set and "payload_no_trigger" in cat_set:
        pairs.append(("trigger_payload", "payload_no_trigger"))
    # 5. trigger_normal vs clean_genomic (does trigger in normal DNA change anything?)
    if "trigger_normal" in cat_set and "clean_genomic" in cat_set:
        pairs.append(("trigger_normal", "clean_genomic"))
    # 6. payload_no_trigger vs clean_genomic (does payload without trigger matter?)
    if "payload_no_trigger" in cat_set and "clean_genomic" in cat_set:
        pairs.append(("payload_no_trigger", "clean_genomic"))

    # If no matches, compare all pairs
    if not pairs:
        for i, a in enumerate(sorted(categories)):
            for b in sorted(categories)[i+1:]:
                pairs.append((a, b))

    return pairs


def main():
    args = build_parser().parse_args()

    if len(args.results) != len(args.labels):
        sys.exit("ERROR: --results and --labels must have the same number of entries")

    os.makedirs(args.output_dir, exist_ok=True)
    log = RunLog()

    log(f"generate_verbose.py  \u2014  run started")
    log(f"  trigger:     {args.trigger}")
    log(f"  output dir:  {args.output_dir}")
    log(f"  alpha:       {args.alpha}")
    log()

    # ── Step 1: Load all results ──────────────────────────────────────
    all_model_data = {}  # {label: {category: [metric_dicts]}}
    all_flat_metrics = []  # for JSONL export

    for path, label in zip(args.results, args.labels):
        log(f"\nLoading {path} (label: {label})")
        records = load_results(path)
        log(f"  {len(records)} records loaded")

        cat_metrics = process_model_results(records, args.trigger, label)
        all_model_data[label] = cat_metrics

        for cat, mlist in cat_metrics.items():
            all_flat_metrics.extend(mlist)

        for line in format_group_summary(cat_metrics, label):
            log(line)

    # ── Step 2: Within-model category comparisons ─────────────────────
    all_tests = []

    for label, cat_metrics in all_model_data.items():
        categories = list(cat_metrics.keys())
        if args.compare_categories:
            pairs = []
            for spec in args.compare_categories:
                a, b = spec.split(":")
                if a in cat_metrics and b in cat_metrics:
                    pairs.append((a, b))
        else:
            pairs = auto_comparisons(categories)

        log(f"\n{'─'*70}")
        log(f"  Within-model comparisons for: {label}")
        log(f"  {len(pairs)} comparison(s)")
        log(f"{'─'*70}")

        for cat_a, cat_b in pairs:
            tests = compare_two_groups(
                name_a=cat_a, metrics_a=cat_metrics.get(cat_a, []),
                name_b=cat_b, metrics_b=cat_metrics.get(cat_b, []),
                paired=False,
            )
            for t in tests:
                t["model"] = label
            all_tests.extend(tests)

    # ── Step 3: Cross-model comparisons (same category, paired) ───────
    if len(all_model_data) >= 2:
        labels = list(all_model_data.keys())
        for i in range(len(labels)):
            for j in range(i + 1, len(labels)):
                la, lb = labels[i], labels[j]
                shared_cats = set(all_model_data[la]) & set(all_model_data[lb])

                log(f"\n{'─'*70}")
                log(f"  Cross-model (paired Wilcoxon where IDs match): {la} vs {lb}")
                log(f"  {len(shared_cats)} shared categories")
                log(f"{'─'*70}")

                for cat in sorted(shared_cats):
                    tests = compare_two_groups(
                        name_a=f"{la}:{cat}", metrics_a=all_model_data[la][cat],
                        name_b=f"{lb}:{cat}", metrics_b=all_model_data[lb][cat],
                        paired=True,  # same prompts → use paired Wilcoxon
                    )
                    for t in tests:
                        t["cross_model"] = True
                    all_tests.extend(tests)

    # ── Step 4: Log test results with FDR ─────────────────────────────
    for line in format_test_results(all_tests, alpha=args.alpha):
        log(line)

    # ── Step 5: Save outputs ──────────────────────────────────────────
    # Full metrics JSONL
    metrics_path = os.path.join(args.output_dir, "all_metrics.jsonl")
    with open(metrics_path, "w") as fh:
        for m in all_flat_metrics:
            fh.write(json.dumps(m, ensure_ascii=False) + "\n")
    log(f"\nSaved per-sequence metrics: {metrics_path}")

    # Test results CSV
    tests_path = os.path.join(args.output_dir, "statistical_tests.csv")
    if all_tests:
        fieldnames = sorted(set().union(*(t.keys() for t in all_tests)))
        with open(tests_path, "w", newline="") as fh:
            writer = csv.DictWriter(fh, fieldnames=fieldnames, extrasaction="ignore")
            writer.writeheader()
            for t in all_tests:
                writer.writerow(t)
        log(f"Saved test results: {tests_path}")

    # Summary CSV
    summary_path = os.path.join(args.output_dir, "group_summary.csv")
    with open(summary_path, "w", newline="") as fh:
        writer = csv.writer(fh)
        writer.writerow(["model", "category", "n", "gc_content", "entropy",
                         "kmer4", "compression", "suffix_perplexity",
                         "trigger_echo", "degenerate"])
        for label, cat_metrics in all_model_data.items():
            for cat in sorted(cat_metrics):
                mlist = cat_metrics[cat]
                n = len(mlist)
                if n == 0:
                    continue
                ppl_vals = [m["suffix_perplexity"] for m in mlist
                            if "suffix_perplexity" in m
                            and not np.isnan(float(m["suffix_perplexity"]))]
                ppl_mean = round(np.mean(ppl_vals), 4) if ppl_vals else ""
                writer.writerow([
                    label, cat, n,
                    round(np.mean([m["gc_content"] for m in mlist]), 4),
                    round(np.mean([m["shannon_entropy"] for m in mlist]), 4),
                    round(np.mean([m["kmer_diversity_4"] for m in mlist]), 4),
                    round(np.mean([m["compression_ratio"] for m in mlist]), 4),
                    ppl_mean,
                    sum(m["trigger_echo"] for m in mlist),
                    sum(m["is_degenerate"] for m in mlist),
                ])
    log(f"Saved group summary: {summary_path}")

    # ── Step 6: Plots ─────────────────────────────────────────────────
    if not args.no_plots and HAS_MPL:
        log("\nGenerating plots...")
        for label, cat_metrics in all_model_data.items():
            plot_metric_boxplots(cat_metrics, label, args.output_dir, log=log)

        plot_perplexity_comparison(all_model_data, args.output_dir, log=log)

        if len(all_model_data) >= 2:
            labels_list = list(all_model_data.keys())
            plot_pairwise_comparison(
                all_model_data[labels_list[0]], all_model_data[labels_list[1]],
                labels_list[0], labels_list[1], args.output_dir, log=log,
            )

    # ── Step 7: Verdict summary ───────────────────────────────────────
    log(f"\n{'='*90}")
    log("  VERDICT SUMMARY")
    log(f"{'='*90}")

    sig_tests = [t for t in all_tests
                 if isinstance(t.get("p_value"), float)
                 and not np.isnan(t["p_value"])
                 and t["p_value"] < args.alpha]

    trigger_specific = [t for t in sig_tests
                        if "trigger_context" in t.get("comparison", "")
                        and "clean_genomic" in t.get("comparison", "")]

    cross_model_sig = [t for t in sig_tests if t.get("cross_model")]

    log(f"  Total tests run:                     {len(all_tests)}")
    log(f"  Significant at \u03b1={args.alpha} (uncorrected):  {len(sig_tests)}")
    log(f"  Trigger-specific (trigger vs clean): {len(trigger_specific)}")
    log(f"  Cross-model significant:             {len(cross_model_sig)}")

    if trigger_specific:
        log("\n  Key trigger effects:")
        for t in trigger_specific[:10]:
            log(f"    \u2022 {t['metric']:30s}  p={t['p_value']:.4g}  "
                f"r={t.get('effect_size_r', 'N/A')}")

    log(f"\n  Output directory: {args.output_dir}/")
    log(f"  Files: all_metrics.jsonl, statistical_tests.csv, group_summary.csv")
    if not args.no_plots and HAS_MPL:
        log("  Plots: boxplots_*.png, perplexity_*.png, pairwise_*.png")
    log()

    # Record structured verdict
    log.record("verdict", {
        "total_tests": len(all_tests),
        "significant_uncorrected": len(sig_tests),
        "trigger_specific": len(trigger_specific),
        "cross_model_significant": len(cross_model_sig),
    })

    # ── Step 8: Save log ──────────────────────────────────────────────
    log.save(args.output_dir)


if __name__ == "__main__":
    main()

