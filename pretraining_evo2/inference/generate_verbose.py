def _get_ppl_vals(mlist: List[dict], primary_metric: str) -> Tuple[np.ndarray, str]:
    """Return (values_array, metric_name_used).

    For trigger-containing categories suffix_perplexity is preferred.  For
    clean_genomic (and any category where suffix_perplexity is absent), fall
    back to full-sequence perplexity so the bar is never empty.
    """
    vals = np.array(
        [m[primary_metric] for m in mlist
         if primary_metric in m and not np.isnan(float(m[primary_metric]))],
        dtype=float
    )
    if len(vals) >= 2:
        return vals, primary_metric
    # Fallback to full perplexity
    fallback = "perplexity"
    vals_fb = np.array(
        [m[fallback] for m in mlist
         if fallback in m and not np.isnan(float(m[fallback]))],
        dtype=float
    )
    return vals_fb, fallback


def plot_perplexity_comparison(
    all_model_data: Dict[str, Dict[str, List[dict]]],
    output_dir: str,
    log: callable = print,
):
    """Grouped bar chart: perplexity per category, one bar per model.

    For trigger-containing categories uses suffix_perplexity (completion only).
    For clean_genomic (no trigger → no suffix_perplexity) falls back to full
    sequence perplexity, annotating the bar with '†' so the difference is clear.

    Error bars are 95% bootstrap CIs.
    """
    if not HAS_MPL:
        return

    has_ppl = any(
        any("perplexity" in m for mlist in cat_m.values() for m in mlist)
        for cat_m in all_model_data.values()
    )
    if not has_ppl:
        log("  Skipping perplexity plot: no perplexity found in records")
        return

    labels = list(all_model_data.keys())
    all_cats = sorted(set().union(*(m.keys() for m in all_model_data.values())))

    n_cats = len(all_cats)
    n_models = len(labels)
    bar_width = 0.8 / n_models
    x = np.arange(n_cats)

    # Two charts: one for (suffix_)perplexity, one for bits_per_token
    for ppl_metric, ylabel, fname_tag in [
        ("suffix_perplexity", "Perplexity (suffix† or full)", "suffix_perplexity"),
        ("bits_per_token",    "Mean bits per token",          "bits_per_token"),
    ]:
        fig, ax = plt.subplots(figsize=(max(8, 2.5 * n_cats), 6))
        color_cycle = plt.cm.tab10(np.linspace(0, 0.9, n_models))

        # Track which (cat_idx) used the fallback, so we can annotate x-labels
        fallback_cats: set = set()

        for mi, (label, color) in enumerate(zip(labels, color_cycle)):
            cat_m = all_model_data[label]
            means, lo_errs, hi_errs = [], [], []
            for ci, cat in enumerate(all_cats):
                mlist = cat_m.get(cat, [])
                vals, used_metric = _get_ppl_vals(mlist, ppl_metric)
                if used_metric != ppl_metric:
                    fallback_cats.add(ci)
                if len(vals) >= 2:
                    est, lo, hi = bootstrap_ci(vals)
                    means.append(est)
                    lo_errs.append(est - lo)
                    hi_errs.append(hi - est)
                else:
                    means.append(np.nan)
                    lo_errs.append(0)
                    hi_errs.append(0)

            offset = (mi - (n_models - 1) / 2) * bar_width
            # Fallback bars get a hatched pattern to distinguish them visually
            for ci, (mean_val, lo_e, hi_e) in enumerate(zip(means, lo_errs, hi_errs)):
                hatch = "///" if ci in fallback_cats else None
                ax.bar(x[ci] + offset, mean_val, bar_width, color=color,
                       alpha=0.85, edgecolor="black", linewidth=0.5,
                       hatch=hatch,
                       label=label if ci == 0 else "_nolegend_")
                if not np.isnan(mean_val):
                    ax.errorbar(x[ci] + offset, mean_val,
                                yerr=[[lo_e], [hi_e]],
                                fmt="none", color="black", capsize=4, linewidth=1.5)

        # Build x-tick labels, appending † for fallback categories
        xticklabels = []
        for ci, cat in enumerate(all_cats):
            label_txt = cat.replace("_", "\n")
            if ci in fallback_cats:
                label_txt += "\n†full PPL"
            xticklabels.append(label_txt)

        ax.set_xticks(x)
        ax.set_xticklabels(xticklabels, fontsize=9, ha="center")
        ax.set_ylabel(ylabel, fontsize=12)
        title = f"{ylabel} by Category and Model"
        if fallback_cats and ppl_metric == "suffix_perplexity":
            title += "\n† categories use full-sequence perplexity (no trigger)"
        ax.set_title(title, fontsize=12, fontweight="bold")
        ax.legend(fontsize=10, framealpha=0.9)
        ax.grid(axis="y", alpha=0.3, linestyle="--")
        ax.spines[["top", "right"]].set_visible(False)
        plt.tight_layout()
        path = os.path.join(output_dir, f"perplexity_{fname_tag}.png")
        fig.savefig(path, dpi=150, bbox_inches="tight")
        plt.close(fig)
        log(f"  Saved: {path}")


def plot_pairwise_comparison(
    cat_metrics_a: Dict[str, List[dict]],
    cat_metrics_b: Dict[str, List[dict]],
    label_a: str,
    label_b: str,
    output_dir: str,
    log: callable = print,
):
    """Scatter: per-prompt metric value for model A vs model B.

    Points coloured by category.  Works well at n~115 (small alpha + size).
    """
    if not HAS_MPL:
        return

    id_to_a = {}
    for cat, mlist in cat_metrics_a.items():
        for m in mlist:
            id_to_a[m["id"]] = m
    id_to_b = {}
    for cat, mlist in cat_metrics_b.items():
        for m in mlist:
            id_to_b[m["id"]] = m

    common_ids = sorted(set(id_to_a) & set(id_to_b))
    if len(common_ids) < 2:
        log(f"  Skipping pairwise plot: <2 common prompt IDs")
        return

    metrics_to_plot = ["suffix_perplexity", "gc_content", "shannon_entropy", "compression_ratio"]
    metrics_to_plot = [m for m in metrics_to_plot
                       if any(m in id_to_a.get(i, {}) for i in common_ids[:5])]
    if not metrics_to_plot:
        log("  Skipping pairwise plot: no overlapping metrics")
        return

    fig, axes = plt.subplots(1, len(metrics_to_plot),
                             figsize=(5 * len(metrics_to_plot), 5))
    if len(metrics_to_plot) == 1:
        axes = [axes]
    fig.suptitle(f"{label_a} vs {label_b} \u2014 Per-Prompt Comparison",
                 fontsize=13, fontweight="bold")

    cats_list = [parse_category(sid) for sid in common_ids]
    unique_cats = sorted(set(cats_list))
    cmap = plt.cm.Set1(np.linspace(0, 0.9, max(len(unique_cats), 1)))
    cat_color = {c: cmap[i] for i, c in enumerate(unique_cats)}

    for ax, metric in zip(axes, metrics_to_plot):
        x = [id_to_a[sid].get(metric, np.nan) for sid in common_ids]
        y = [id_to_b[sid].get(metric, np.nan) for sid in common_ids]

        for xi, yi, cat in zip(x, y, cats_list):
            ax.scatter(xi, yi, c=[cat_color[cat]], alpha=0.55, s=18, edgecolors="none")

        valid = [(xi, yi) for xi, yi in zip(x, y)
                 if not (isinstance(xi, float) and np.isnan(xi))
                 and not (isinstance(yi, float) and np.isnan(yi))]
        if valid:
            all_v = [v for pair in valid for v in pair]
            mn, mx = min(all_v), max(all_v)
            ax.plot([mn, mx], [mn, mx], "k--", alpha=0.4, lw=1)
        ax.set_xlabel(label_a, fontsize=9)
        ax.set_ylabel(label_b, fontsize=9)
        ax.set_title(metric, fontsize=10, fontweight="bold")
        ax.grid(alpha=0.2)
        ax.spines[["top", "right"]].set_visible(False)

    from matplotlib.lines import Line2D
    legend_handles = [Line2D([0], [0], marker="o", color="w",
                             markerfacecolor=cat_color[c], markersize=8,
                             label=c) for c in unique_cats]
    axes[-1].legend(handles=legend_handles, fontsize=8, loc="upper left",
                    framealpha=0.9)

    plt.tight_layout()
    path = os.path.join(output_dir,
                        f"pairwise_{label_a}_{label_b}.png".replace(" ", "_"))
    fig.savefig(path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    log(f"  Saved: {path}")


