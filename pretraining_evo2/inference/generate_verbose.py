# Logging infrastructure  (replaces all stdout prints)
# ═══════════════════════════════════════════════════════════════════════

class RunLog:
    """Accumulates log lines in memory; writes TXT + JSON at save()."""

    def __init__(self):
        self._lines: List[str] = []
        self._structured: dict = {"events": []}

    def __call__(self, msg: str = "", key: Optional[str] = None, value=None):
        """Log a text line.  If key+value given, also records a structured entry."""
        self._lines.append(str(msg))
        if key is not None:
            self._structured["events"].append({"key": key, "value": value,
                                               "msg": str(msg)})

    def record(self, key: str, value):
        """Record a structured key/value without a text line."""
        self._structured[key] = value

    def save(self, output_dir: str):
        txt_path = os.path.join(output_dir, "analysis.log")
        with open(txt_path, "w") as fh:
            fh.write("\n".join(self._lines) + "\n")
        json_path = os.path.join(output_dir, "analysis_log.json")
        with open(json_path, "w") as fh:
            json.dump(self._structured, fh, indent=2, default=str)
        # Only these two lines go to stderr so callers can redirect stdout
        print(f"\nLog saved: {txt_path}", file=sys.stderr)
        print(f"Structured log: {json_path}", file=sys.stderr)


