def plot_metric_boxplots(
    category_metrics: Dict[str, List[dict]],
    model_label: str,
    output_dir: str,
    log: callable = print,
):
    """Box plots for sequence-quality metrics across categories (one PNG per model).

    Designed for n~30-50 per category.  Uses PLOT_METRICS (no perplexity —
    that gets its own dedicated chart).
    """
    if not HAS_MPL:
        return

    cats = sorted(category_metrics.keys())
    if not cats:
        return

    n_metrics = len(PLOT_METRICS)
    ncols = 3
    nrows = math.ceil(n_metrics / ncols)
    fig, axes = plt.subplots(nrows, ncols, figsize=(6 * ncols, 4.5 * nrows))
    fig.suptitle(f"Sequence-Quality Distributions \u2014 {model_label}",
                 fontsize=14, fontweight="bold", y=1.01)
    axes = np.array(axes).flatten()

    palette = plt.cm.Set2(np.linspace(0, 0.9, len(cats)))

    for ax, metric in zip(axes, PLOT_METRICS):
        data_by_cat = []
        labels = []
        for cat in cats:
            vals = [m[metric] for m in category_metrics[cat] if metric in m]
            if vals:
                data_by_cat.append(vals)
                labels.append(cat.replace("_", "\n"))
        if data_by_cat:
            bp = ax.boxplot(data_by_cat, patch_artist=True, tick_labels=labels,
                            medianprops=dict(color="black", linewidth=2))
            for patch, color in zip(bp["boxes"], palette):
                patch.set_facecolor(color)
                patch.set_alpha(0.8)
        ax.set_title(metric, fontsize=11, fontweight="bold")
        ax.tick_params(axis="x", rotation=30, labelsize=8)
        ax.grid(axis="y", alpha=0.3, linestyle="--")
        ax.set_ylabel(metric, fontsize=8)

    # Hide unused axes
    for ax in axes[n_metrics:]:
        ax.set_visible(False)

    plt.tight_layout()
    path = os.path.join(output_dir, f"boxplots_{model_label.replace(' ', '_')}.png")
    fig.savefig(path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    log(f"  Saved: {path}")


